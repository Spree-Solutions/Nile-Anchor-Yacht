export const getYachtById = (id) => {
  return yachts.find(yacht => yacht.id === id);
};

export const getYachtBySlug = (slug) => {
  return yachts.find(yacht => yacht.slug === slug);
};

export const getAvailableYachts = () => {
  return yachts.filter(yacht => yacht.availability === 'Available');
};

export const getYachtsByCapacity = (minCapacity) => {
  return yachts.filter(yacht => yacht.capacity.guests >= minCapacity);
};